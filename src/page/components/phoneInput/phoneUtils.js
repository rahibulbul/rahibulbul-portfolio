export const getCountryFromPhoneNumber = (phoneNumber) => {
  // Default country code and flag (can be adjusted as needed)
  let defaultCountry = { countryCode: "44", flag: "GB" };

  if (!phoneNumber) return defaultCountry;

  // Check based on the initial digits of the phone number
  for (const country of countryData) {
    if (phoneNumber.startsWith(country.dialCode)) {
      return { countryCode: country.dialCode, flag: country.flag };
    }
  }

  return defaultCountry; // Fallback to default if no match
};

